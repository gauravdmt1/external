class addMultiplyThreeNumbers
{
  static int add(int a,int b,int c)
  {
    return a + b + c;
  }
  static int multiply(int a,int b,int c)
  {
    return a * b * c;
  }
  public static void main(String args[])
  {
    System.out.println("Addition function : " + add(2,3,4));
    System.out.println("Multiplication function : " + multiply(2,3,4));
  }
}
